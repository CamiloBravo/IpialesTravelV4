    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();
        FragmentManager fm = getSupportFragmentManager();
        FragmentTransaction ft = fm.beginTransaction();
        //noinspection SimplifiableIfStatement
        if (id == R.id.sitios) {
            Sitio1 fragment = new Sitio1();
            ft.replace(android.R.id.content, fragment).commit();
        }
        if (id == R.id.sitios) {
            Sitio2 fragment = new Sitio2();
            ft.replace(android.R.id.content, fragment).commit();
        }
        if (id == R.id.sitios) {
            Sitio3 fragment = new Sitio3();
            ft.replace(android.R.id.content, fragment).commit();
        }
        switch (id)
        {
            case R.id.Principal: //este tambien esta en menu.xml
                intent =new Intent(SitiosActivity.this, MainActivity.class); //ojo a lo que antepone el this!!!
                intent.putExtra("username", username);
                intent.putExtra("correo", correo);
                startActivity(intent);
                finish();
                break;
            case R.id.parques: //este tambien esta en menu.xml
                intent =new Intent(SitiosActivity.this, ListActivity.class); //ojo a lo que antepone el this!!!
                intent.putExtra("username", username);
                intent.putExtra("correo", correo);
                startActivity(intent);
                finish();
                break;
            case R.id.miPerfil: //este tambien esta en menu.xml
                intent =new Intent(SitiosActivity.this, PerfilActivity.class); //ojo a lo que antepone el this!!!
                intent.putExtra("username", username);
                intent.putExtra("correo", correo);
                startActivity(intent);
                finish();
                break;
            case R.id.bares: //cerrar es nombre de menu.xml
                intent =new Intent(SitiosActivity.this, BaresActivity.class); //ojo a lo que antepone el this!!!
                intent.putExtra("username", username);
                intent.putExtra("correo", correo);
                startActivity(intent);
                finish();
                break;
            case R.id.hotel: //cerrar es nombre de menu.xml
                intent =new Intent(SitiosActivity.this, HotelActivity.class); //ojo a lo que antepone el this!!!
                intent.putExtra("username", username);
                intent.putExtra("correo", correo);
                startActivity(intent);
                finish();
                break;
            case R.id.cerrar: //cerrar es nombre de menu.xml
                editor.putInt("login",-1); //aqui coloca el -1, para que quede sin login
                editor.commit();
                intent =new Intent(SitiosActivity.this, LoginActivity.class); //ojo a lo que antepone el this!!!
                startActivity(intent);
                finish();
                break;
        }

        return super.onOptionsItemSelected(item);
    }
